#标准运算符：加（+）；减（—）；乘（*）；除（/）；整除（//）只取整数部分
print(1+1)#加法运算
print(2-1)#减法运算
print(2*1)#乘法运算
print(1/2)#除法运算
print(11/2)#除法运算
print(11//2)#整除运算：只取商的整数部分
#取余运算符：%
print(11%2)#当余数小于除数时输出余数
#幂运算符：**
print(2**2)#表示2的2次方
print(2**3)#表示2的3次方
#一正一负向下取整：只针对整除运算
print(9//4)#2
print(-9//-4)#2
print(-9//4)#-3,向下取整
print(9//-4)#-3,向下取整
#一正一负要遵循公式：余数=被除数-除数*商
print(-9%4)#3
print(9%-4)#-3
#一正一负取余公式中的商是一正一负取整运算中的商