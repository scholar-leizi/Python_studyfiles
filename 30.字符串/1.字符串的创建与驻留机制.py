'''
字符串：
    在Python中字符串是基本数据类型，是一个把不可变的字符序列
什么叫字符串驻留机制：
    仅保留一份相同且不可变字符串的方法，不同的值被存放在字符串的驻留池中，Python的驻留机制对相同的字符串只保留一份拷贝，后续创建相同字符串时，不会
    开辟新空间，而是把该字符串的地址赋值给创建的变量
驻留机制的几种情况（交互模式）：
    字符串的长度为0或1时
    符合标识符的字符串
    字符串只在编译时进行驻留，而非运行时
    必须是[-5,256]之间的整数数字
字符串驻留机制的优缺点：
    当需要值相同的字符串时，可以直接从字符串池中拿来使用，避免频繁的创建和销毁，提升效率和节省内存，因此拼接字符串和修改字符串是会比较影响性能的
    在需要字符串拼接时建议使用str的join方法，而非+，因为join()方法是先计算出所有字符串中的长度，然后再拷贝，只new一次对象，效率要比“+”效率高
'''
a='Python'
b="Python"
c='''Python'''
print(a,id(a))
print(b,id(b))
print(c,id(c))